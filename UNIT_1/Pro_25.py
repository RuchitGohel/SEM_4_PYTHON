# pro-25: demo for import module in Python

import math

x = 5.7
y = 5
z = 16

floor_value = math.floor(x)
print(f"Floor value of {x}: {floor_value}")

ceil_value = math.ceil(x)
print(f"Ceiling value of {x} : {ceil_value}")

factorial_value = math.factorial(y)
print(f"Factorial  value of {y} : {factorial_value}")

sqrt_value = math.sqrt(z)
print(f"Square root value of {z} : {sqrt_value}")
